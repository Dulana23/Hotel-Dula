    private void AddInformationLoadDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddInformationLoadDataBtnActionPerformed
                       try {
                         
                           ResultSet rs = MySQL.executeSearch("SELECT * FROM `invoice_table` INNER JOIN `unit` ON `invoice_table`.`unit_id` = `unit`.`id` ORDER BY `no` ASC; " );
            
                          JTable table = invoicePanel.getInvoiceInfoTable();
                          DefaultTableModel dtm = (DefaultTableModel)table.getModel();
                          dtm.setRowCount(0);
            
                         while (rs.next()) {                
                            Vector<String> v = new Vector<>();
                
                                v.add(rs.getString("no"));
                                v.add(rs.getString("description"));
                                v.add(rs.getString("name"));
                                v.add(rs.getString("unit_price"));
                                v.add(rs.getString("quantity"));
                                v.add(rs.getString("amount"));
                
               
                            dtm.addRow(v);
                         }
            
                       } catch (java.sql.SQLException e) {
                           
                  }
    }//GEN-LAST:event_AddInformationLoadDataBtnActionPerformed

    private void AddInformationSubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddInformationSubmitBtnActionPerformed
              
              String no           = InfoIDInput.getText();
              String description  = InfoDescriptionInput.getText();
              
              String unit_name      = (String) InfoCategoryIDComboBox.getSelectedItem();
              int unit_id           = unitMap.get(unit_name);
              
              String unit_price   = InfoUnitPriceInput.getText();
              String quantity     = InfoItemQuantityInput.getText();
              String amount       = InfoAmountInput.getText();
              
              
              try {
              MySQL.executeIUD("INSERT INTO `invoice_table`(`no`,`description`,`unit_id`,`unit_price`,`quantity`,`amount`) VALUES"
                       + "('"+no+"','"+description+"','"+unit_id+"','"+unit_price+"','"+quantity+"','"+amount+"')");
               
                JOptionPane.showMessageDialog(this,"Informations Add Successfully !!!","Success Message",JOptionPane.INFORMATION_MESSAGE);
                
        } catch (Exception e) {
             e.printStackTrace();
        }
    }//GEN-LAST:event_AddInformationSubmitBtnActionPerformed

    private void RemoveInfoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveInfoBtnActionPerformed
            try {
                         int choise = JOptionPane.showConfirmDialog(null, "Do you want to Delete Details", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                                  switch (choise) {
                                      case JOptionPane.YES_OPTION:
                                               MySQL.executeIUD("DELETE FROM `invoice_table`");
                                              
                                               JTable table = invoicePanel.getInvoiceInfoTable();
                                               DefaultTableModel dtm = (DefaultTableModel)table.getModel();
                                              dtm.setRowCount(0);
                                         break;
                                      case JOptionPane.NO_OPTION:
                                         break;
                                      default:
                                          
                                    }
                          
                           
                     
        } catch (Exception e) {
             e.printStackTrace();
        }
           
    }//GEN-LAST:event_RemoveInfoBtnActionPerformed

