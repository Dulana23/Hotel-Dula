    public void loadRoomTypeDetails(){
           try {
              ResultSet rs = MySQL.executeSearch("SELECT * FROM `unit`" );
            
            Vector <String> v = new Vector<>();  
            v.add("Select");
            while (rs.next()) {
                int id = rs.getInt("id");
                String name = rs.getString("name");
                 v.add(name);
                 
                 unitMap.put(name, id);
                
            }
            DefaultComboBoxModel<String> dcm = new DefaultComboBoxModel(v);
            InfoCategoryIDComboBox.setModel(dcm);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }
