    private void AddNewRoomLoadDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNewRoomLoadDataBtnActionPerformed
        try {
            ResultSet rs = MySQL.executeSearch("SELECT * FROM `room` INNER JOIN `room_type` ON `room`.`room_type_id` = `room_type`.`id` "
                + " INNER JOIN `status` ON `room`.`status_id` = `status`.`status_id` ORDER BY `room_id` ASC;" );

            JTable table = roomsPanel.getRoomsInfoTable();
            DefaultTableModel dtm = (DefaultTableModel)table.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector<String> v = new Vector<>();
                v.add(rs.getString("room_id"));
                v.add(rs.getString("room_number"));
                v.add(rs.getString("room_type"));
                v.add(rs.getString("status_name"));
                v.add(rs.getString("maximun_count"));

                dtm.addRow(v);
            }

        } catch (java.sql.SQLException e) {
        }
    }//GEN-LAST:event_AddNewRoomLoadDataBtnActionPerformed

    private void AddNewRoomSubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNewRoomSubmitBtnActionPerformed
        String roomID        = AddRoomIDInput.getText();
        String roomNumber    = AddRoomNumberInput.getText();
        
        String roomType     = (String) RoomTypeComboBox.getSelectedItem();
        int roomID1          = roomsMap.get(roomType);
        
        String room_status   = (String) RoomStatusComboBox.getSelectedItem();
        int   roomStatusID   = statusMap.get(room_status);
        
        String maximun_count = MaxCountInput.getText();

        try {
            MySQL.executeIUD("INSERT INTO `room`(`room_id`,`room_number`,`room_type_id`,`status_id`,`maximun_count`) VALUES"
                + "('"+roomID+"','"+roomNumber+"','"+roomID1+"','"+roomStatusID+"','"+maximun_count+"')");

            JOptionPane.showMessageDialog(this,"Room Registration Succeess","Success Message",JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_AddNewRoomSubmitBtnActionPerformed

