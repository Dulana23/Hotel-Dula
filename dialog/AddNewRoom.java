    public void loadRoomDetails(){
          try {
              ResultSet rs = MySQL.executeSearch("SELECT * FROM `room_type`" );
            
            Vector <String> v = new Vector<>();  
            v.add("Select");
            while (rs.next()) {
                int id      = rs.getInt("id");
                String name = rs.getString("room_type");
                 v.add(name);
                 
                 roomsMap.put(name, id);
                
            }
            DefaultComboBoxModel<String> dcm = new DefaultComboBoxModel(v);
            RoomTypeComboBox.setModel(dcm);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }
    
