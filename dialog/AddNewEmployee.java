    private void AddNewEmployeeSubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNewEmployeeSubmitBtnActionPerformed
              String employeeID   = EmployeeIDInput.getText();
              String firstName    = EmployeeFirstNameInput.getText();
              String lastName     = EmployeeLastNameInput.getText();
              String email        = EmployeeEmailInput.getText();
              String mobile       = EmployeeMobileInput.getText();
              
              String sectionName    = (String) SectionIDComboBox.getSelectedItem();
              int sectionID         = statusMap.get(sectionName);
              
              String statusName     = (String) StatusIDComboBox.getSelectedItem();
              int statusID          = statusMap1.get(statusName);
              
              try {
              MySQL.executeIUD("INSERT INTO `employee`(`employee_id`,`first_name`,`last_name`,`email`,`mobile`,`section_id`,`status_id`) VALUES"
                       + "('"+employeeID+"','"+firstName+"','"+lastName+"','"+email+"','"+mobile+"','"+sectionID+"','"+statusID+"')");
               
                JOptionPane.showMessageDialog(this,"New Employee Add Succeess!!!","Success Message",JOptionPane.INFORMATION_MESSAGE);
                
        } catch (Exception e) {
             e.printStackTrace();
        }
    
    }//GEN-LAST:event_AddNewEmployeeSubmitBtnActionPerformed

    private void AddNewEmployeeLoadDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNewEmployeeLoadDataBtnActionPerformed
                   try {
                    ResultSet rs = MySQL.executeSearch("SELECT * FROM `employee` INNER JOIN `section` ON `employee`.`section_id` = `section`.`id` "
                    + " INNER JOIN `status` ON `employee`.`status_id` = `status`.`status_id` ORDER BY `employee_id` ASC;" );
            
                     JTable table = employeePanel.getEmployeeInfoTable();  
                     DefaultTableModel dtm = (DefaultTableModel)table.getModel();
                   dtm.setRowCount(0);
            
            while (rs.next()) {                
                Vector<String> v = new Vector<>();
                v.add(rs.getString("employee_id"));
                v.add(rs.getString("first_name") + " " + rs.getString("last_name"));
                v.add(rs.getString("email"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("section_name"));
                v.add(rs.getString("status_name"));
              
               
                dtm.addRow(v);
            }
            
        } catch (java.sql.SQLException e) {
        }
    
    }//GEN-LAST:event_AddNewEmployeeLoadDataBtnActionPerformed

