    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String InvoiceID =(String) InvoiceIDComboBox.getSelectedItem();
        String statusName = (String) StatusIDComboBox.getSelectedItem();
        int statusID     = statusMap.get(statusName);
        String statusName1 = String.valueOf(statusID);

        try {

            String sql = "UPDATE financial SET `status_id` = ? WHERE `invoice_id` = ?";
            String[] params = { statusName1, InvoiceID};

            int result = MySQL.executeUpdate(sql, params);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, " Updated Successfully!!!", "Updated Message", JOptionPane.INFORMATION_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(this, "Update failed");

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

